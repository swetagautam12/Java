import java.util.*;
public class Whileloop {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println("Enter n: ");
        int n = sc.nextInt();
        int counter = 1;
        int sum =0;
        while (counter<=n){
            
            
            sum = sum+counter;
            counter++;
            // System.out.println(sum);
        }
        System.out.println(sum);

    }
    
    
}